// Get all unique categories
export const getAllCategories = (): FoodCategory[] => {
  const categories = new Set<FoodCategory>();
  foodDatabase.forEach(food => categories.add(food.category));
  return Array.from(categories).sort();
};

// Search and filter utilities
export const searchFoods = (query: string): FoodItem[] => {
  const lower = query.toLowerCase();
  return foodDatabase.filter(food =>
    food.name.toLowerCase().includes(lower) ||
    food.category.toLowerCase().includes(lower)
  );
};

export const getFoodsByCategory = (category: FoodCategory): FoodItem[] => {
  return foodDatabase.filter(food => food.category === category);
};

export const getFoodById = (id: string): FoodItem | undefined => {
  return foodDatabase.find(food => food.id === id);
};
