export type FoodCategory = 
  | "Fruits"
  | "Vegetables"
  | "Grains & Cereals"
  | "Legumes & Pulses"
  | "Dairy & Alternatives"
  | "Meat & Protein"
  | "Nuts & Seeds"
  | "Oils & Fats"
  | "Beverages"
  | "Snacks & Others";

export type ImpactLevel = "Low" | "Medium" | "High";
export type HealthRating = "Excellent" | "Good" | "Moderate" | "Limited";

export interface NutritionInfo {
  calories: number;
  protein: number; // grams
  fat: number; // grams
  carbs: number; // grams
  fiber: number; // grams
  vitamins: string[];
}

export interface FoodItem {
  id: string;
  name: string;
  category: FoodCategory;
  emoji?: string;
  co2PerServing: number; // kg CO2e per serving
  servingSize: string; // e.g., "1 medium apple"
  servingGrams: number; // grams
  nutrition: NutritionInfo;
  seasonality: number[]; // 0-11 for Jan-Dec
  sustainabilityNote: string;
  ecoScore: ImpactLevel;
  healthScore: HealthRating;
  advice: string;
  waterFootprint?: number; // liters per serving
}

// Helper function to determine eco impact
const getEcoScore = (co2: number): ImpactLevel => {
  if (co2 < 0.5) return "Low";
  if (co2 < 1.5) return "Medium";
  return "High";
};

const getHealthScore = (nutrition: NutritionInfo): HealthRating => {
  const score = 
    (nutrition.fiber / 10) * 25 + 
    (nutrition.protein / 20) * 25 + 
    ((nutrition.vitamins.length / 5) * 25) +
    (nutrition.carbs / 50) * 25;
  
  if (score >= 80) return "Excellent";
  if (score >= 60) return "Good";
  if (score >= 40) return "Moderate";
  return "Limited";
};

